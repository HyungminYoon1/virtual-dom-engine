function createLocaleMessages(definition) {
  return Object.freeze(definition);
}

function isPlainObject(value) {
  return value !== null && typeof value === "object" && !Array.isArray(value);
}

function mergeLocaleMessages(base, overrides) {
  const result = { ...base };

  for (const key of Object.keys(overrides)) {
    const baseValue = base[key];
    const overrideValue = overrides[key];

    if (isPlainObject(baseValue) && isPlainObject(overrideValue)) {
      result[key] = mergeLocaleMessages(baseValue, overrideValue);
      continue;
    }

    result[key] = overrideValue;
  }

  return result;
}

export function resolveSupportedLocale(input) {
  const rawValue = Array.isArray(input) ? input.find(Boolean) : input;

  if (typeof rawValue !== "string" || rawValue.trim() === "") {
    return "en";
  }

  const normalized = rawValue.toLowerCase();

  if (normalized.startsWith("ko")) {
    return "ko";
  }

  if (normalized.startsWith("ja")) {
    return "ja";
  }

  if (normalized.startsWith("zh")) {
    return "zh";
  }

  if (normalized.startsWith("es")) {
    return "es";
  }

  return "en";
}

export function getLocaleMessages(locale) {
  if (!MESSAGES[locale] || locale === "en") {
    return MESSAGES.en;
  }

  return mergeLocaleMessages(MESSAGES.en, MESSAGES[locale]);
}

